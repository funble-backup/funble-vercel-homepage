function main() {
  console.log("=== FAQ Seed ===");

  // Clear existing FAQ data
  db.exec("DELETE FROM faqs; DELETE FROM faq_categories;");

  // Update categories
  const catStmt = db.prepare(
    "INSERT OR REPLACE INTO faq_categories (name, code, sort_order) VALUES (?, ?, ?)"
  );
  for (let i = 0; i < CATEGORIES.length; i++) {
    catStmt.run(CATEGORIES[i].name, CATEGORIES[i].code, i);
  }
  console.log(`${CATEGORIES.length} categories inserted`);

  // Insert FAQs
  const getCatId = db.prepare("SELECT id FROM faq_categories WHERE code = ?");
  const faqStmt = db.prepare(
    "INSERT INTO faqs (category_id, question, answer, sort_order) VALUES (?, ?, ?, ?)"
  );

  let totalFaqs = 0;
  for (const [code, items] of Object.entries(FAQ_DATA)) {
    const catRow = getCatId.get(code) as { id: number } | undefined;
    if (!catRow) {
      console.log(`  Category ${code} not found, skipping`);
      continue;
    }
    for (let i = 0; i < items.length; i++) {
      faqStmt.run(catRow.id, items[i].q, items[i].a, i);
      totalFaqs++;
    }
    console.log(`  ${code}: ${items.length} FAQs`);
  }

  console.log(`\nTotal: ${totalFaqs} FAQs inserted`);
  db.close();
}

main();
